using Microsoft.EntityFrameworkCore;
using SplitIt.Data;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();
builder.Services.AddCors(options =>
{
    options.AddPolicy("AllowAll",
        builder =>
        {
            builder.AllowAnyOrigin()
                   .AllowAnyMethod()
                   .AllowAnyHeader();
        });
});
builder.Services.AddDbContext<SplitItDbContext>(options =>
    options.UseNpgsql(builder.Configuration.GetConnectionString("DefaultConnection")));


var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.MapGet("/api/saludos", () => "Hola desde Minimal API en .NET!");
app.MapGet("/api/saludos/{nombre}", (string nombre) =>
{
    if (string.IsNullOrWhiteSpace(nombre))
    {
        return Results.BadRequest("El nombre no puede estar vacio");
    }
    return Results.Ok($"Hola, {nombre}!");
});

app.Run();

